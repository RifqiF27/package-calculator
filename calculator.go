package calculator

func Penjumlahan(a, b int) int {
	result := a + b
	return result
}
func Perkalian(a, b int) int {
	result := a * b
	return result
}
func Pembagian(a, b int) float64 {
	result := float64(a) / float64(b)
	return result
}
func Pengurangan(a, b int) int {
	result := a - b
	return result
}